# Using remove() method
list=["John","David","Mike"]
list.remove("John")
print(list)

# Using pop() method
list=["John","David","Mike"]
print(list.pop(1))
print(list)

# Using del keywords
list=["John","David","Mike"]
# del list[2]
del list
print(list)
# clear a list using clear() method
list=["John","David","Mike"]
list.clear()
print(list)